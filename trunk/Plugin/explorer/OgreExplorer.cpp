#include "OgreExplorer.h"

namespace Ogre
{
	//////////////////////////////////////////////////////////////////////////
	//
	//////////////////////////////////////////////////////////////////////////
	Explorer::Explorer(const String& sName, const String& sPath)
		:m_sName(sName),m_sPath(sPath)
	{
	}

	//////////////////////////////////////////////////////////////////////////
	Explorer::~Explorer(void)
	{
	}

	//////////////////////////////////////////////////////////////////////////
	void		Explorer::setPath(const String& sPath)
	{
		m_sPath = sPath;
	}

	//////////////////////////////////////////////////////////////////////////
	String		Explorer::getPath() const
	{
		return m_sPath;
	}

	//////////////////////////////////////////////////////////////////////////
	void		Explorer::setName(const String& sName)
	{
		m_sName = sName;
	}

	//////////////////////////////////////////////////////////////////////////
	String		Explorer::getName() const
	{
		return m_sName;
	}

}
